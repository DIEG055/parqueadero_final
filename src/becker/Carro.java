package becker;


public class Carro {
 // atributos
  private String placa;
  private double tiempo;
//atributos

    public Carro(String placa, double tiempo) {
        this.placa = placa;
        this.tiempo= tiempo;
    }

    public String getPlaca() {
        return placa;
    }

    public double getTiempo() {
        return tiempo;
    }
  
    
    
}
